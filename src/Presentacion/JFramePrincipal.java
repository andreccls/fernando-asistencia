    private void Inicio() {

        Controlador auxDrive = new Controlador();
        try {

            if (auxDrive.getColegio().equals(null)) {
                JOptionPane.showMessageDialog(null, "Debe ingresar Colegio");
            } else {
                auxDrive.getColegio();
                Drive = auxDrive;
            }

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex.toString());
        }
    }

