                        pe.setCalle(jTextField6.getText().toUpperCase());
                        pe.setAltura(alt);
                        pe.setPiso(jTextField8.getText().toUpperCase());
                        pe.setDepto(jTextField13.getText().toUpperCase());
                        pe.setCorreoElectronico(jTextField9.getText());
                        pe.setSexo(sexo);
                        pe.setEstadoCivil(civil);
